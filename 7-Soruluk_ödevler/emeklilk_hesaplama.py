def emeklılık_durumu():
    emeklılık_yası = 65

    yas = int(input("Lütfen Yaşınızı Giriniz: "))
    yas1 = yas
    if yas1 < emeklılık_yası:
        kalan_yıl = emeklılık_yası - yas1
        print("emekliliğinize " , kalan_yıl , " yasınız var")

    else:
        yas1 == emeklılık_yası
        print("zaten emkli oldunuz...")


emeklılık_durumu()


